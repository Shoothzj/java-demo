package com.github.shoothzj.demo.jpath;

import com.jayway.jsonpath.DocumentContext;
import com.jayway.jsonpath.JsonPath;
import lombok.extern.slf4j.Slf4j;
import org.junit.jupiter.api.Test;

/**
 * @author hezhangjian
 */
@Slf4j
public class JpathExample {

    String json = "{\n" +
            "    \"store\": {\n" +
            "        \"book\": [\n" +
            "            {\n" +
            "                \"category\": \"reference\",\n" +
            "                \"author\": \"Nigel Rees\",\n" +
            "                \"title\": \"Sayings of the Century\",\n" +
            "                \"price\": 8.95\n" +
            "            },\n" +
            "            {\n" +
            "                \"category\": \"fiction\",\n" +
            "                \"author\": \"Evelyn Waugh\",\n" +
            "                \"title\": \"Sword of Honour\",\n" +
            "                \"price\": 12.99\n" +
            "            },\n" +
            "            {\n" +
            "                \"category\": \"fiction\",\n" +
            "                \"author\": \"Herman Melville\",\n" +
            "                \"title\": \"Moby Dick\",\n" +
            "                \"isbn\": \"0-553-21311-3\",\n" +
            "                \"price\": 8.99\n" +
            "            },\n" +
            "            {\n" +
            "                \"category\": \"fiction\",\n" +
            "                \"author\": \"J. R. R. Tolkien\",\n" +
            "                \"title\": \"The Lord of the Rings\",\n" +
            "                \"isbn\": \"0-395-19395-8\",\n" +
            "                \"price\": 22.99\n" +
            "            }\n" +
            "        ],\n" +
            "        \"bicycle\": {\n" +
            "            \"color\": \"red\",\n" +
            "            \"price\": 19.95\n" +
            "        }\n" +
            "    },\n" +
            "    \"expensive\": 10\n" +
            "}";

    @Test
    public void testJPath() {
        DocumentContext documentContext = JsonPath.parse(json);
        JsonPath jsonPath = JsonPath.compile("$..author");
        {
            Object read = documentContext.read("$..author");
            System.out.println(read);
        }
        {
            Object read = documentContext.read(jsonPath);
            System.out.println(read);
        }
    }

    @Test
    public void testJPathBuilder() {
        DocumentContext documentContext = JsonPath.parse("");
    }

    @Test
    public void testJPathJson() {
        String data = "{\"a\":\"{\\\"a\\\":\\\"\\\"}\"}";
        DocumentContext documentContext = JsonPath.parse(data);
        Object read = documentContext.read("$.a.a");
        System.out.println(read);
    }

}
